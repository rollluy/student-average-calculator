    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        String v = "";
        outputField.setText("");
        int test1, test2, test3, test4;
        String firstN = firstName.getText(), lastN = lastName.getText(), testOne = avg1.getText(), testTwo = avg2.getText(), testThree = avg3.getText(), testFour = avg4.getText();
        
        //if (testOne.equals("") || testTwo.equals("") || testThree.equals("") || testFour.equals("")) {
        //    outputField.setText("Please input 4 test grades.");
        if (!isNumeric(testOne)) {
            avg1.setText("");
            outputField.setText("Please enter 4 numeric grades.");
            return;
        }
        if (!isNumeric(testTwo)) {
            avg2.setText("");
            outputField.setText("Please enter 4 numeric grades.");
            return;
        }
        if (!isNumeric(testThree)) {
            avg3.setText("");
            outputField.setText("Please enter 4 numeric grades.");
            return;
        }

        if (!isNumeric(testFour)) {
            avg4.setText("");
            outputField.setText("Please enter 4 numeric grades.");
            return;
        }
        test1 = Integer.parseInt(testOne);
        test2 = Integer.parseInt(testTwo);
        test3 = Integer.parseInt(testThree);
        test4 = Integer.parseInt(testFour);
        if (studentIndex == 30) {
            outputField.setText("Class is at capacity.");
            firstName.setText("");
            lastName.setText("");
        } else if (firstN.equals("") || lastN.equals("")) {
            outputField.setText("Please input a first and last name for the student.");
            firstName.setText("");
            lastName.setText("");
        //} else if (testOne.equals("") || testTwo.equals("") || testThree.equals("") || testFour.equals("")) {
            //outputField.setText("Please input 4 test grades.");
        } else if (test1 > 100 || test1 < 0 || test2 > 100 || test2 < 0 || test3 > 100 || test3 < 0 || test4 > 100 || test4 < 0) {
            outputField.setText("All grades must be of valid range.");
        } else {
            for (int a = 0; a < studentIndex; a++) {
                if (firstN.equals(classList[a][0]) && lastN.equals(classList[a][1])) {
                    outputField.setText("This student already exists in the class. Please input a new student.");
                    firstName.setText("");
                    lastName.setText("");
                    return;
                }
            }
            String[] student = {firstN, lastN, testOne, testTwo, testThree, testFour};
            classList[studentIndex] = student;
            studentIndex++;
            for (int i = 0; i < studentIndex; i++) {
                v += "Student " + (i + 1) + ":  " + classList[i][0] + " " + classList[i][1] + "  Grade 1: " + classList[i][2] + " Grade 2: " + classList[i][3] + " Grade 3: " + classList[i][4] + " Grade 4: " + classList[i][5] + "\n";
            }
            displayArea.setText(v);
        }

    }//GEN-LAST:event_addButtonActionPerformed

