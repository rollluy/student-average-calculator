    private void studentAverageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_studentAverageActionPerformed
        // TODO add your handling code here:
        boolean studentExist = false;
        String x = firstName.getText(), y = lastName.getText(), m;
        double studentAverage = 0;
        int a, b, c, d;
        for (int i = 0; i < studentIndex; i++) {
            if (x.equals(classList[i][0]) && y.equals(classList[i][1])) {
                a = Integer.parseInt(classList[i][2]);
                b = Integer.parseInt(classList[i][3]);
                c = Integer.parseInt(classList[i][4]);
                d = Integer.parseInt(classList[i][5]);
                studentAverage = Math.round(((a + b + c + d) / 4.00)*100.0)/100.0;
                studentExist = true;
                m = Double.toString(studentAverage);
                outputField.setText("The average for this student is " + m + ".");
            }
        }
        if (studentExist == false) {
            outputField.setText("This student does not exist in this class.");
            firstName.setText("");
            lastName.setText("");
        }
    }//GEN-LAST:event_studentAverageActionPerformed

    private void courseAverageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_courseAverageActionPerformed
        // TODO add your handling code here:
        double courseAverage = 0, studentAverage = 0;
        int a, b, c, d;
        for (int i = 0; i < studentIndex; i++) {
            a = Integer.parseInt(classList[i][2]);
            b = Integer.parseInt(classList[i][3]);
            c = Integer.parseInt(classList[i][4]);
            d = Integer.parseInt(classList[i][5]);
            studentAverage += (a + b + c + d) / 4.00;
        }
        courseAverage = Math.round((studentAverage / studentIndex)*100.0)/100.0;
        String l = Double.toString(courseAverage);
        outputField.setText("The current course average is " + l + ".");
    }//GEN-LAST:event_courseAverageActionPerformed

